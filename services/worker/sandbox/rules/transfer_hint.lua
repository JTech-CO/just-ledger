-- transfer_hint.lua — 이체 힌트 태깅.
--
-- 상대처/적요에 이체·송금 표기가 있는 거래를 "이체" 로 태깅한다.
--
-- 주의(INV-8): 이체 '페어' 확정 매칭은 Prolog 서비스가 담당한다(거짓양성 0, 금액
-- 근사 매칭 금지). 이 규칙은 단일 거래에 힌트 태그만 붙일 뿐, 상대 거래와 링크하거나
-- transfer_link 를 생성하지 않는다.

